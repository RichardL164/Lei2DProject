public class Entity {
    private int xCoord;
    private int yCoord;
    private int speed;

    public int getspeed() {
        return speed;
    }

    public int getxCoord() {
        return xCoord;
    }

    public int getyCoord() {
        return yCoord;
    }

    public void setX(int xCoord) {
        this.xCoord = xCoord;
    }

    public void setY(int yCoord) {
        this.yCoord = yCoord;
    }

    public void setspeed(int speed) {
        this.speed = speed;
    }
}
