public class Entity {
    private int xCoord;
    private int yCoord;
    private int distance;

    public int getDistance() {
        return distance;
    }

    public int getxCoord() {
        return xCoord;
    }

    public int getyCoord() {
        return yCoord;
    }

    public void setX(int xCoord) {
        this.xCoord = xCoord;
    }

    public void setY(int yCoord) {
        this.yCoord = yCoord;
    }

    public void setDistance(int distance) {
        this.distance = distance;
    }
}
